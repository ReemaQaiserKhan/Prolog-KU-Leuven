/*?- change(a,[b,c,d],X).
X = [(a,b), (a,c), (a,d)]. 
 */
 
 %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
 change(E,[H|T]):-
    change2(E,[H|T],L),
    write('['),
    round(L),write(']').

round([]).
round([H|T]):-
    H=[L1,L2],
    write('('),write(L1),write(L2),write(')'),
    round(T).

change2(E,[],[]).
change2(E,[H|T],[H2|P]):-
    append([E],[H],H2),
    change2(E,T,P).
    
