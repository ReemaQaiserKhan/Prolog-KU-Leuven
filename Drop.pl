/* Drop every kth element in a list.
*/

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
drop([H|T],P,Y):-
    drop1([H|T],P,K,_),
    flatten(K,Y).

drop1(R,_,[],R).
drop1([H|T],P,[H2|L],R):-
    P1 is P-1,
    drop2([H|T],P1,H2,[_|Rest]),
    drop1(Rest,P,L,R).

drop2(K,0,[],K).
drop2([H|T],N,[H|L],Rest):-
    N1 is N-1,
    drop2(T,N1,L,Rest).
    
